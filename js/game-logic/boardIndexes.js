//BUILDS AND RETURNS AN ARRAY OF 0-90 WHEN CALLED
//** HELPER FUNCTION
export function boardHexes() {
    let arrayOfHexes = []
    for(let i = 0; i < 91; i++) {
        arrayOfHexes.push(i)
    }
    return arrayOfHexes
}
//RETURNS AN ARRAY WITH THE ROW AND COLUMN WHERE 0-90 IS LOCATED IN ARRAY MATRIX
//** HELPER FUNCTION
function buildArrayOfAllHexIndexes(matrix) {
    let indexArray = []
    boardHexes().forEach(element => {
        for(let i = 0; i < matrix.length; i++) {
            for(let j = 0; j < matrix[i].length; j++) {
                if(matrix[i][j] === element) {
                    let index = {
                        row: i,
                        column: j
                    }
                    indexArray.push(index)
                }    
            }
        }
    })
    return indexArray
}

